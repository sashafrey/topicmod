#include "rpcz/clock.hpp"
#include <sys/time.h>

namespace rpcz {

uint64 zclock_time(void) {
    struct timeval tv;
    gettimeofday (&tv, NULL);
    return (int64_t) (tv.tv_sec * 1000 + tv.tv_usec / 1000);
}

}  // namespace rpcz
